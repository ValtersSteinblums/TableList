import UIKit

class MovieTableViewController: UITableViewController {

    var movies = Movie.createMovie()
    
    override func viewDidLoad() {
        super.viewDidLoad()
    }

    // MARK: - Table view data source

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return movies.count
    }


    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "movieListCell", for: indexPath) as! MovieTableViewCell

        // Configure the cell...
        let movie = movies[indexPath.row]
        
        cell.movieTitleCell?.text = movie.title
        cell.movieGenreCell?.text = movie.genre
        cell.movieYearCell?.text = String(movie.year)
        cell.moviePosterCell?.image = UIImage(named: movie.poster)

        return cell
    }
    
